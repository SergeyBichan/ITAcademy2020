package shop.good;

import java.util.GregorianCalendar;

public class Good {
    private int id;
    private String name;
    private GregorianCalendar date;


    public Good(int id, String name, GregorianCalendar date) {
        this.id = id;
        this.name = name;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public GregorianCalendar getDate() {
        return date;
    }

    public void setDate(GregorianCalendar date) {
        this.date = date;
    }
}
